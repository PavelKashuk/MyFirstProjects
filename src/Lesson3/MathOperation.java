package Lesson3;

public class MathOperation {
    public int sum (int first, int second) {
        return first + second;
    }
    public int sub (int first, int second) {
        return first - second;
    }
    public static double mult (double first, double second) {
        return first * second;
    }
    public static double div (double first, double second) {
        return first / second;
    }
}
